            Boliviana, su labor está enmarcada de acuerdo a las decisiones de los Congresos y Conferencias Nacionales de
            Universidades y a las gestiones encomendadas por las casas de Estudio Superiores; además cumple funciones de
            representación especialmente entre poderes del Estado.</p>
